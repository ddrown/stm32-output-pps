int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART1_UART_Init();
  MX_TIM1_Init();
  MX_ADC_Init();
  MX_TIM3_Init();

  /* USER CODE BEGIN 2 */
  HAL_ADCEx_Calibration_Start(&hadc);
  start_rx_uart();
  timer_start();
  write_uart_s("pwm output starting\n");
  cmdline_prompt();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
    uint16_t next_adc = 50; // start at 50ms
    while(1) {
      while(__HAL_TIM_GET_COUNTER(&htim1) < next_adc) {
        while(uart_rx_ready()) {
          cmdline_addchr(uart_rx_data());
        }
      }
      adc_poll();
      next_adc += 100; // every 100ms
      if(next_adc > 1000) {
        break;
      }
    }
    // happens after 950ms
    update_adc();

    adjust_pps();

    // wait till wrap to 0ms
    while(__HAL_TIM_GET_COUNTER(&htim1) >= 950) {
      while(uart_rx_ready()) {
        cmdline_addchr(uart_rx_data());
      }
    }
  }
  /* USER CODE END 3 */

}

